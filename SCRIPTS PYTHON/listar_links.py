def google_drive_to_direct(link):
    # Extrai o ID do arquivo do link do Google Drive
    try:
        file_id = link.split('/d/')[1].split('/')[0]
        return f'https://drive.google.com/uc?export=view&id={file_id}'
    except Exception:
        return link

